from dataclasses import dataclass

@dataclass

class Person:
    full_name: str = None
    firstname: str = None
    lastname: str = None
    username: str = None
    age: int = None
    salary: int = None
    department: str = None
    email: str = None
    address: str = None
    mobile: str = None
    password: str = None
    address_buffalo: str = None
